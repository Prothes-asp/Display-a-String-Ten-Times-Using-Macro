INCLUDE 'EMU8086.INC'

PRINT_MSG MACRO
    
    MOV CX, 10
    MOV AH, 9

    START:
    PRINTN "HELLO WORLD"

    LOOP START

ENDM

.MODEL SMALL
.STACK  100H
.DATA
  MSG1 DB "HELLO WORLD$"
.CODE

MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    PRINT_MSG

    MOV AX, 4CH
    INT 21H

    MAIN ENDP
END MAIN
